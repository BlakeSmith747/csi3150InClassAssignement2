function showImgA() {
  let imgA = document.querySelector(".imgA");
  imgA.style.visibility = "visible";
  let hide_imgA = document.querySelector(".hide_imgA");
  hide_imgA.style.visibility = "visible";
}

function hideImgA() {
  let imgA = document.querySelector(".imgA");
  imgA.style.visibility = "hidden";
  let hide_imgA = document.querySelector(".hide_imgA");
  hide_imgA.style.visibility = "hidden";
}

